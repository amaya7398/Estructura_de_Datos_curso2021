

public class Prioridad {
    char signo;
    int valor;
 
public Prioridad(char signo, int valor){
    this.signo=signo;
    this.valor=valor;
}
public char getSigno() {
    return signo;
}
public int getValor(){
    return valor;
}
public void setSigno(char signo){
    this.signo=signo;
}
public void setSigno(int valor){
    this.valor=valor;
}



}
